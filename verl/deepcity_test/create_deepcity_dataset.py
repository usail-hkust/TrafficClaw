#!/usr/bin/env python3
"""
Create DeepCity trigger dataset for verl training.

This script creates a minimal parquet dataset that triggers DeepCity Agent Loop.
The actual optimization context (checkpoint, traffic states, etc.) is dynamically
generated by DeepCityMaster during interaction.

Usage:
    python create_deepcity_dataset.py --train 450
    python create_deepcity_dataset.py --train 100  # Small test
    python create_deepcity_dataset.py --train 2000  # Large scale
    python create_deepcity_dataset.py --val-envs 6 --val-steps 6  # 36 val samples
"""

import argparse
import os
import pandas as pd


def create_dataset(num_samples: int, output_path: str, prompt: str):
    """Create a parquet dataset with uniform trigger prompts (for training).
    
    Args:
        num_samples: Number of samples to generate
        output_path: Output parquet file path
        prompt: Trigger prompt text
    """
    # Store as message list format (required by verl's agent loop)
    # Each prompt is a list containing a single user message
    # agent_name specifies which agent loop to use (registered via @register decorator)
    
    #   Add sample_id field to distinguish samples
    # Multiple workers (rollout.n) for the same sample use the same sample_id to connect to the same Master
    prompts = []
    sample_ids = []
    for i in range(num_samples):
        sample_prompt = [
            {"role": "user", "content": prompt}  # Uniform prompt, does not contain Sample ID
        ]
        prompts.append(sample_prompt)
        sample_ids.append(f"sample_{i}")  # Sample ID
    
    data = {
        "prompt": prompts,
        "sample_id": sample_ids,  # New field
        "agent_name": ["deepcity_agent"] * num_samples  # Use DeepCityAgentLoop
    }
    
    df = pd.DataFrame(data)
    
    # Create parent directory if not exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    # Save to parquet
    df.to_parquet(output_path, index=False)
    
    print(f"  Created {output_path}: {len(df)} samples")
    return len(df)


def create_val_dataset(num_envs: int, num_steps: int, output_path: str):
    """Create a val dataset with distinct prompts per (env, step).
    
    Val dataset: each sample has a different prompt identifier (env_idx + step_idx),
    used to distinguish rewards of different environments and time steps in process_validation_metrics.
    
    Samples arranged by env (not by step), ensuring that 6 samples in the same batch
    are assigned to 6 different val Masters (since _get_val_master_id routes by batch_index % num_val_masters).
    
    Arrangement order (when val_batch_size=num_envs):
      Batch 0: env_0/step_0, env_1/step_0, ..., env_5/step_0  → 6 Masters each advance 1 step
      Batch 1: env_0/step_1, env_1/step_1, ..., env_5/step_1  → 6 Masters each advance 1 more step
      ...
      Batch 5: env_0/step_5, env_1/step_5, ..., env_5/step_5  → Complete 3h simulation
    
    Args:
        num_envs: Number of val environments (default: 6)
        num_steps: Number of time steps per environment (default: 6)
        output_path: Output parquet file path
    """
    num_samples = num_envs * num_steps
    prompts = []
    sample_ids = []
    
    # Arrange in step-major order: each batch contains the same step for all envs
    for step_idx in range(num_steps):
        for env_idx in range(num_envs):
            sample_prompt = [
                {"role": "user", "content": f"[Val] env={env_idx} step={step_idx}"}
            ]
            prompts.append(sample_prompt)
            sample_ids.append(f"val_env{env_idx}_step{step_idx}")
    
    data = {
        "prompt": prompts,
        "sample_id": sample_ids,
        "agent_name": ["deepcity_agent"] * num_samples
    }
    
    df = pd.DataFrame(data)
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    df.to_parquet(output_path, index=False)
    
    print(f"  Created {output_path}: {len(df)} val samples ({num_envs} envs × {num_steps} steps)")
    return len(df)


def main():
    parser = argparse.ArgumentParser(description="Create DeepCity trigger dataset")
    parser.add_argument(
        "--train",
        type=int,
        default=450,
        help="Number of training samples (default: 450)"
    )
    parser.add_argument(
        "--val-envs",
        type=int,
        default=6,
        help="Number of val environments (default: 6, must match num_val_masters)"
    )
    parser.add_argument(
        "--val-steps",
        type=int,
        default=6,
        help="Number of time steps per val environment (default: 6, = val_simulation_duration / checkpoint_interval)"
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default="data",
        help="Output directory relative to script location (default: data)"
    )
    parser.add_argument(
        "--prompt",
        type=str,
        default="Start DeepCity transportation optimization",
        help="Trigger prompt text"
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=4,
        help="Training batch size for calculating steps (default: 4)"
    )
    parser.add_argument(
        "--epochs",
        type=int,
        default=10,
        help="Total epochs for calculating steps (default: 10)"
    )
    
    args = parser.parse_args()
    
    # Create output directory
    output_dir = args.output_dir
    os.makedirs(output_dir, exist_ok=True)
    
    # Create train dataset
    train_path = os.path.join(output_dir, "deepcity_train.parquet")
    train_samples = create_dataset(args.train, train_path, args.prompt)
    
    # Create val dataset (distinct prompts per env × step)
    val_path = os.path.join(output_dir, "deepcity_val.parquet")
    val_samples = create_val_dataset(args.val_envs, args.val_steps, val_path)
    
    # Calculate training statistics
    steps_per_epoch = train_samples // args.batch_size
    total_steps = steps_per_epoch * args.epochs
    
    print("\n" + "="*60)
    print("📊 Dataset Statistics")
    print("="*60)
    print(f"Train samples:        {train_samples}")
    print(f"Val samples:          {val_samples} ({args.val_envs} envs × {args.val_steps} steps)")
    print(f"Val batch size:       {args.val_envs} (= num_val_masters)")
    print(f"Val batches:          {args.val_steps}")
    print(f"Total samples:        {train_samples + val_samples}")
    print(f"\nBatch size:           {args.batch_size}")
    print(f"Steps per epoch:      {steps_per_epoch}")
    print(f"Total epochs:         {args.epochs}")
    print(f"Total training steps: {total_steps}")
    print("="*60)
    print(f"\n  Dataset files created in: {os.path.abspath(output_dir)}")
    print(f"   - {train_path}")
    print(f"   - {val_path}")
    print("\n💡 Update your training script with:")
    print(f"   data.train_files={os.path.abspath(train_path)}")
    print(f"   data.val_files={os.path.abspath(val_path)}")


if __name__ == "__main__":
    main()
